/**
 * Medical Document Page Classifier - Node.js Implementation v2.0
 *
 * Estratégia 100% Node.js (sem binários do sistema):
 * - PDFs com texto nativo: extração direta com pdfjs-dist por página
 * - PDFs escaneados: renderização com pdfjs-dist + @napi-rs/canvas, OCR via LLM
 * - JPEG/JPG: LLM vision diretamente
 * - Compatível com produção (sem poppler-utils, ghostscript, etc.)
 */

import * as pdfParseModule from "pdf-parse";
const pdfParse = (pdfParseModule as any).default ?? pdfParseModule;

import sharp from "sharp";
